#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i * i != nb)
	{
		if (i * i > nb || i >= 46341)
			return (0);
		i++;
	}
	return (i);
}